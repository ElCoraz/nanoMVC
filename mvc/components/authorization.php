<?php
//********************************************************************************** */
class Authorization
{
    //****************************************************************************** */
    static function start()
    {
        session_start();
    }
    //****************************************************************************** */
    static function setId($id)
    {
        if (!isset($_SESSION['id'])) {
            $_SESSION['id'] = $id;
        }
    }
    //****************************************************************************** */
    static function close()
    {
        unset($_SESSION);
        session_destroy();
    }
    //****************************************************************************** */
    static function IsAuthorization()
    {
        return isset($_SESSION['id']);
    }
    //****************************************************************************** */
}
//********************************************************************************** */